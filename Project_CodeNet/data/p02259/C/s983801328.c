#include "stdio.h"


int main()
{
    int N, a, A[1000],j,x;
    scanf("%d", &N);

    for (a = 0; a <= N - 1; a++)
        scanf("%d", &A[a]);

    int flag = 1;
    int i = 0;
    while (flag) {
       flag = 0;
        for (j = N - 1;  j > i; j--) {
            if (A[j] < A[j - 1]) {
                x = A[j];
                A[j] = A[j - 1];
                A[j - 1] = x;
                flag = 1;
            }
         
        }
        i++;
    }
   

    for (j = 0; j < N - 1; j++)
        printf("%d ", A[j]);

    printf("%d\n", A[N - 1]);
    printf("%d", i);
    return 0;
}